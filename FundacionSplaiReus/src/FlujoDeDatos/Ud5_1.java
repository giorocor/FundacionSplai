package FlujoDeDatos;


public class Ud5_1 {
	public static void main(String args[]) {
		int x=4, y=3;
		
		if(x==y) {
			System.out.println("El valor "+x+" es igual a "+y);
		}else {
				if(x<y) {
					System.out.println("El valor "+x+" es menor a "+y);
				}else if(x>y) {
					System.out.println("El valor "+x+" es mayor a "+y);
				}
			}
		}
	}
